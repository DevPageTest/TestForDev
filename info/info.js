function populatePage() {
  document.title = info.title;
  document.querySelector(".header_navigation-home").href = info.homeBtn.href;
  document.querySelector(".header_navigation-home--logo").src =
    info.homeBtn.src;
  document.querySelector(".header_navigation-home--logo").alt = "Logo";
  //Top links and dropdown
  const navLinks = document.querySelector(".header_navigation-options-anchors");
  info.navLinks.forEach((item) => {
    const listItem = document.createElement("li");
    listItem.innerHTML = `<a href="${item.title}">${item.title}</a>`;
    navLinks.appendChild(listItem);
  });

  document.querySelector(".dropbtn").textContent = info.dropdownBtn.title;

  const dropLinks = document.querySelector(".dropdown-content");
  info.dropdownBtn.dropdownLinks.forEach((item) => {
    const listItem = document.createElement("a");
    listItem.classList.add("dropdown-content-a");
    listItem.setAttribute("href", `${item.href}`);
    listItem.innerHTML = `${item.title}`;
    dropLinks.appendChild(listItem);
  });

  const socialLinks = document.querySelector(
    ".header_navigation-options-socials"
  );
  info.socialsBtns.forEach((item) => {
    const listItem = document.createElement("li");
    listItem.innerHTML = `<a class="socialBtn" href="${item.href}"><img alt="${item.alt}" src="${item.src}"/></a>`;
    socialLinks.appendChild(listItem);
  });

  document.querySelector(".header_text-title").textContent =
    info.headerTitle.text;
  document.querySelector(".header_text-second").textContent =
    info.headerSecondText.text;
  document.querySelector(".header_background").src = info.backgroundImage.src;
  document.querySelector(".header_background").alt = info.backgroundImage.alt;
  document.querySelector(".header_image").src = info.backgroundSideImage.src;
  document.querySelector(".header_image").alt = info.backgroundSideImage.alt;
  document.querySelector(".activities_container-title").textContent =
    info.activitiesTitle.text;

  const activities = document.querySelector(".activities_container-list");
  info.activitiesList.forEach((item, index) => {
    const listItem = document.createElement("li");
    listItem.classList.add("activities_container-list--item");
    if (index === 3) {
      listItem.classList.add("image");
      listItem.innerHTML = `            
      <img
        class="activities_image"
        src="${item.imgsrc}"
        alt="${item.imgalt}"
      />`;
    } else {
      listItem.innerHTML = `            
    <div class="list_image_container">
        <img
          class="list_image"
          src="${item.src}"
          alt="${item.alt}"
        />
    </div>
    <div class="list_text_container">
      <h2 class="list_title">${item.title}</h2>
      <p class="list_text">${item.text}</p>
    </div>`;
    }
    activities.appendChild(listItem);
  });

  document.querySelector(".activities_container-info").innerHTML = `
    <img
      class="activities_container-info--img"
      src="${info.activitiesContainerInfo.src}"
      alt="${info.activitiesContainerInfo.alt}"
    />
    <div class="activities_container-info--text">
      <p>${info.activitiesContainerInfo.text}</p>
    </div>
  `;
  document.querySelector(".information_discord").innerHTML = `
    <img src="assets/images/discord-white.png" alt="Discord logo" />
    <h1>${info.activitiesSocials.discordSocials.title}</h1>
    <a href="${info.activitiesSocials.discordSocials.href}">https://discord.gg</a>
  `;
  document.querySelector(".social_title").textContent =
    info.activitiesSocials.discordSocials.secondTitle;

  const infoLinks = document.querySelector(".socials_imgs");
  info.activitiesSocials.discordSocials.socials.forEach((item) => {
    const listItem = document.createElement("a");
    listItem.setAttribute("href", `${item.href}`);
    listItem.innerHTML = `<img src="${item.src}" alt="${item.alt}" />`;
    infoLinks.appendChild(listItem);
  });

  document.querySelector(".plataforms_title").textContent =
    info.platforms.title;

  const platforms = document.querySelector(".platforms_ul");
  info.platforms.list.forEach((item) => {
    const listItem = document.createElement("li");
    listItem.classList.add("platforms_li");
    listItem.innerHTML = `
    <a href="${item.href}" style="background-image: url(assets/images/back.jpg)">
        <div class="platforms_li-container">
          <img src="${item.src}" alt="${item.alt}" />
          <h2>${item.title}</h2>
        </div>
        <div class="platforms_li-text">
          <div class="platforms_li-text--txtbtn">
            <p>${item.text}</p>
            <button>${item.btnText}</button>
          </div>
        </div>
    </a>
    `;
    platforms.appendChild(listItem);
  });

  document
    .querySelector(".side_img")
    .setAttribute("src", `${info.communities.img.src}`);
  document
    .querySelector(".side_img")
    .setAttribute("alt", `${info.communities.img.alt}`);

  const carousel = document.querySelector(".carousel");
  info.communities.carousel.forEach((item) => {
    const listItem = document.createElement("li");
    listItem.classList.add("slide");
    console.log(item.img);
    listItem.innerHTML = `
    <div class="slide-img">
    <img
      src="${item.img.src}"
      alt="${item.img.alt}"
      draggable="false"
    />
    </div>
    <div class="slide-paragraph">
      <h2>${item.title}</h2>
      <p>${item.text}</p>
      <a href="${item.button.href}">${item.button.text}</a>
    </div>
      `;
    carousel.appendChild(listItem);
  });
}

document.addEventListener("DOMContentLoaded", populatePage);
